-- 1. Diaper Categories
CREATE TABLE public.diaper_categories (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT UNIQUE NOT NULL,
  slug TEXT UNIQUE NOT NULL,
  description TEXT DEFAULT '',
  icon TEXT DEFAULT '',
  display_order INTEGER DEFAULT 0
);

ALTER TABLE public.diaper_categories ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public read diaper_categories" ON public.diaper_categories FOR SELECT USING (true);
CREATE POLICY "Full access diaper_categories" ON public.diaper_categories FOR ALL USING (true);

