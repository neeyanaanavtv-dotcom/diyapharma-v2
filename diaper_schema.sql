-- 2. Diaper Products
CREATE TABLE public.diaper_products (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,
  brand TEXT NOT NULL,
  category_id BIGINT REFERENCES public.diaper_categories(id),
  description TEXT DEFAULT '',
  features TEXT[] DEFAULT '{}',
  sizes JSONB DEFAULT '[]',
  image_main TEXT DEFAULT '',
  image_side TEXT DEFAULT '',
  image_back TEXT DEFAULT '',
  image_top TEXT DEFAULT '',
  image_pack TEXT DEFAULT '',
  rating DECIMAL(2,1) DEFAULT 4.5,
  in_stock BOOLEAN DEFAULT true,
  is_new BOOLEAN DEFAULT false,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- sizes column stores JSON array like:
-- [{"size":"M","mrp":599,"sale_price":499,"pack_qty":10},{"size":"L","mrp":649,"sale_price":549,"pack_qty":10}]

ALTER TABLE public.diaper_products ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public read diaper_products" ON public.diaper_products FOR SELECT USING (true);
CREATE POLICY "Full access diaper_products" ON public.diaper_products FOR ALL USING (true);


